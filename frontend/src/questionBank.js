// ─── Question selection logic ──────────────────────────────────────

/**
 * Get questions for a specific role based on difficulty level.
 * All difficulty levels return 15 questions in proper interview order.
 *
 * @param {string} role - Role key from questionBank (or 'general')
 * @param {string} difficulty - 'easy' | 'medium' | 'hard'
 * @returns {Array} Selected questions in interview order
 */
export function getLocalQuestions(role, difficulty = 'medium') {
  const bank = questionBank[role] || questionBank['general'];
  let picks;
  if (difficulty === 'easy') picks = EASY_PICKS;
  else if (difficulty === 'hard') picks = HARD_PICKS;
  else picks = MEDIUM_PICKS;
  return picks.map(i => bank[i]).filter(Boolean);
}

/**
 * Get all available role keys (excluding 'general').
 * @returns {string[]}
 */
export function getAvailableRoles() {
  return Object.keys(questionBank).filter(k => k !== 'general');
}

/**
 * Get questions with repetition control and difficulty-based selection.
 * All levels return 15 questions in proper interview order.
 * Avoids repeating the exact same set in consecutive sessions.
 *
 * @param {string} role - Role key from questionBank (or 'general')
 * @param {string} difficulty - 'easy' | 'medium' | 'hard'
 * @returns {Array} Selected questions in interview order
 */
export function getSmartQuestions(role, difficulty = 'medium', userId = null) {
  const bank = questionBank[role] || questionBank['general'];
  const storageKey = userId ? `interview-${userId}-recent-${role}-${difficulty}` : `iq-recent-${role}-${difficulty}`;
  let recentSessions = [];
  try { recentSessions = JSON.parse(localStorage.getItem(storageKey) || '[]'); } catch { /* ignore */ }
  // Support old format (flat array of IDs) and new format (array of arrays)
  const recentIds = new Set(
    Array.isArray(recentSessions[0]) ? recentSessions.flat() : recentSessions
  );

  let picks;
  if (difficulty === 'easy') picks = EASY_PICKS.slice();
  else if (difficulty === 'hard') picks = HARD_PICKS.slice();
  else picks = MEDIUM_PICKS.slice();

  // Repetition control: swap recently-used questions with same-tag alternatives
  if (recentIds.size > 0) {
    const allIndices = Array.from({ length: bank.length }, (_, i) => i);
    const unusedIndices = allIndices.filter(i => !picks.includes(i) && bank[i]);

    for (let p = 0; p < picks.length; p++) {
      // Never swap intro (first) or closing (last) — they anchor the interview
      if (p === 0 || p === picks.length - 1) continue;

      const q = bank[picks[p]];
      if (q && recentIds.has(q.id)) {
        const currentTag = categoryToTag(q.category);
        // Prefer same-tag swap, fall back to any unused question
        let swapIdx = unusedIndices.find(ui => {
          const uq = bank[ui];
          return uq && categoryToTag(uq.category) === currentTag && !picks.includes(ui);
        });
        if (swapIdx === undefined) {
          swapIdx = unusedIndices.find(ui => !picks.includes(ui));
        }
        if (swapIdx !== undefined) {
          picks[p] = swapIdx;
          const uidx = unusedIndices.indexOf(swapIdx);
          if (uidx >= 0) unusedIndices.splice(uidx, 1);
        }
      }
    }
  }

  // Shuffle middle positions (keep intro at 0 and closing at end) for variety
  const middle = picks.slice(1, -1);
  for (let i = middle.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [middle[i], middle[j]] = [middle[j], middle[i]];
  }

  // Sort middle by phase priority so phases flow Opening → Middle → Ending
  // (preserves randomization within each phase group)
  const openingCats = ['introduction', 'motivation_role', 'motivation_company'];
  const endingCats = ['goals', 'closing'];
  middle.sort((a, b) => {
    const catA = bank[a]?.category || '';
    const catB = bank[b]?.category || '';
    const pA = openingCats.includes(catA) ? 0 : endingCats.includes(catA) ? 2 : 1;
    const pB = openingCats.includes(catB) ? 0 : endingCats.includes(catB) ? 2 : 1;
    return pA - pB;
  });

  picks = [picks[0], ...middle, picks[picks.length - 1]];

  const selected = picks.map(i => bank[i]).filter(Boolean);
  const selectedIds = selected.map(q => q.id);

  // Store last 2 sessions for repeat-avoidance
  const newSessions = [selectedIds];
  if (Array.isArray(recentSessions[0])) {
    newSessions.push(recentSessions[0]); // keep previous session
  } else if (recentSessions.length > 0) {
    newSessions.push(recentSessions); // migrate old format
  }
  try { localStorage.setItem(storageKey, JSON.stringify(newSessions.slice(0, 2))); } catch { /* ignore */ }

  return selected;
}

